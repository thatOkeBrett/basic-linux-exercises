To run a command as administrator (user "root"), use "sudo <command>".
See "man sudo_root" for details.

dee@dee-Vostro-1015:~$ ls
Desktop  Documents  Downloads  Music  Pictures  Public  Templates  Videos
dee@dee-Vostro-1015:~$ pwd
/home/dee
dee@dee-Vostro-1015:~$ mkdir workspace
dee@dee-Vostro-1015:~$ ls
Desktop    Downloads  Pictures  Templates  workspace
Documents  Music      Public    Videos
dee@dee-Vostro-1015:~$ touch README.md
dee@dee-Vostro-1015:~$ cp README.md CHANGELOG.md
dee@dee-Vostro-1015:~$ ls
CHANGELOG.md  Documents  Music     Public     Templates  workspace
Desktop       Downloads  Pictures  README.md  Videos
dee@dee-Vostro-1015:~$ 
